#include <iostream>

using namespace std;

int main()
{
    double x,y;
    cin>>x>>y;
    double sum=x+y;
    double mult=x*y;
    double sub=x-y;
    cout<<x<<" + "<<y<<" = "<<sum<<endl;
    cout<<x<<" * "<<y<<" = "<<mult<<endl;
    cout<<x<<" - "<<y<<" = "<<sub<<endl;

    return 0;
}
